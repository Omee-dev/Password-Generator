def process():
    obj = Password(int(size.get()),luds=[l.get(),u.get(),d.get(),s.get()])
    text.set(obj.give_password())
    T = Entry(root,textvariable=text,font="Consolas 12",width=25,bg='#ff9933',state="readonly").place(x=38,y=150)
